public class Questão13 {

    public static void main(String[] args) {

        int numero = 10;

        if (numero >= 0 && numero <= 10) {
            System.out.println("O número " + numero + " está entre 0 e 10.");
        }
        else {
            System.out.println("O número " + numero + " não está entre 0 e 10.");
        }
    }
}
